export const FUNDING_DICTIONARY = {
  uk: { ...uk, ...FUNNEL_I18N_UK },
  en: { ...en, ...FUNNEL_I18N_EN },
};

/**
 * Persistent locale state hook.
 * Unified: defers to the global LanguageContext so a single language switcher
 * controls the whole app (funding / treasury / command-center included).
 */
export function useFundingLang() {
  const { lang, setLang } = useLang();
  return { lang, setLang };
}

/**
 * Translator factory.  Pass {ctx, lang} explicitly for tests or use the
 * convenience hook below for components inside the Funding Center.
 */
export function makeFundingT(lang) {
  const dict = FUNDING_DICTIONARY[lang] || FUNDING_DICTIONARY.uk;
  return (key, fallback, vars) => {
    let val = Object.prototype.hasOwnProperty.call(dict, key)
      ? dict[key]
      : (FUNDING_DICTIONARY.en[key] !== undefined ? FUNDING_DICTIONARY.en[key] : (fallback ?? key));
    if (vars && typeof val === 'string') {
      Object.entries(vars).forEach(([k, v]) => {
        val = val.replace(new RegExp(`{${k}}`, 'g'), String(v));
      });
    }
    return val;
  };
}

export function useFundingT(forcedLang) {
  const { lang } = useFundingLang();
  const effective = forcedLang || lang;
  return useMemo(() => makeFundingT(effective), [effective]);
}
